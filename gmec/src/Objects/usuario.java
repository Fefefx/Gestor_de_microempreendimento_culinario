package Objects;

/**
 *
 * @author felipe
 */
public class usuario {
    private String user, senha;

    public String getUser() {
        return user;
    }

    public void setUser(String user) {
        this.user = user;
    }

    public String getSenha() {
        return senha;
    }

    public void setSenha(String senha) {
        this.senha = senha;
    }

    public usuario(String user, String senha) {
        this.user = user;
        this.senha = senha;
    }
    
}
