    private void Tab_vendasMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_Tab_vendasMouseClicked
        int linha = Tab_vendas.getSelectedRow();
        vendas venda = new vendas();
        venda.setCodigo(Integer.parseInt(Tab_vendas.getValueAt(linha, 0).toString()));
        venda.setDataVenda(Tab_vendas.getValueAt(linha, 1).toString());
        venda.setTotal(Float.parseFloat(Tab_vendas.getValueAt(linha, 2).toString()));
        this.setVisible(false);
        vendaPresencial vendaP = new vendaPresencial(this, rootPaneCheckingEnabled);
        vendaP.arrumaTela(venda);
        vendaP.setVisible(true);
        CT_data.setText("");
        arrumaTabela();
        this.setVisible(true);
    }//GEN-LAST:event_Tab_vendasMouseClicked

