    public void arrumaTabela() {
        ArrayList resultado = vendasControl.validarDataPesquisa(CT_data.getText());
        if (resultado != null) {
            DefaultTableModel modelo = (DefaultTableModel) Tab_vendas.getModel();
            while (modelo.getRowCount() != 0) {
                modelo.removeRow(0);
            }
            for (int i = 0; i < resultado.size(); i++) {
                vendas vendasv = new vendas();
                vendasv = (vendas) resultado.get(i);
                String[] linha = new String[3];
                linha[0] = String.valueOf(vendasv.getCodigo());
                linha[1] = vendasv.getDataVenda();
                linha[2] = String.valueOf(vendasv.getTotal());
                modelo.addRow(linha);
                Tab_vendas.setModel(modelo);
            }
        } else {
            System.out.println("\nNão há vendas cadastradas");
        }
    }

    public void arrumarTela() {
        CT_data.setText("");
        CT_data.requestFocus();
        //aplicarMascara();
        arrumaTabela();
    }
    
    public void aplicarMascara() {
        try {
            MaskFormatter mascara = new MaskFormatter("##/##/####");
            mascara.install(CT_data);
        } catch (ParseException ex) {
            Logger.getLogger(vendaPresencialteste.class.getName()).log(Level.SEVERE, null, ex);
        }
        arrumaTabela();
    }
    
